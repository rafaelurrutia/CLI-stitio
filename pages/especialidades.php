              <p>El tabaco es la primera causa de muerte prevenible en el mundo y es el responsable del 90% de los cánceres de pulmón, además de enfermedades coronarias y vasculares cerebrales.<br>Clínica Santa María cuenta con un Programa Antitabaco, integrado por un equipo multidisciplinario, destinado a ayudar a quienes quieren dejar de fumar.<br>
              El programa considera la consejería médica, para motivar el autocuidado del paciente y terapia conductual para modificar este hábito y mejorar estilos de vida. Además, realiza terapia farmacológica y otro tipo de estrategias, como practicar deporte, mantener una dieta sana y balanceada, realizar vida en familia y dedicar tiempo a la meditación y relajación.</p>

