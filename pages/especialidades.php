              <h3>Cuida tu corazón</h3>

              <p>Muchas personas pasan inadvertidos los consejos para la salud emocional y creen que la salud del corazón sólo requiere de ejercicio, dieta y medicina, pero no es del todo así. Diversos estudios han demostrado el poder curativo de una actitud positiva y de la risa.</p>

              <p>La relación entre el estrés y las enfermedades cardiovasculares está sólidamente establecida en la bibliografía científica y tiene aceptación cultural.</p>
 
              <p>El concepto de estrés es amplio y, en este caso, abarca un ∑ de emociones negativas, condiciones o experiencias adversas, así como formas inadecuadas o conflictivas de la capacidad de respuesta frente a problemas.</p>
 
              <p>En los últimos años ha crecido el interés por estudiar si la capacidad de disfrutar de la vida, vivir en bienestar o tener una mirada optimista se relaciona con un riesgo menor de morbimortalidad general y cardiovascular. Para ello, se definió como "afecto positivo" un estado y/o estilo placentero de vinculación con el medio, que genera sentimientos tales como felicidad, alegría, animación, entusiasmo y satisfacción.</p>
 
              <p>Resultados de diversos estudios internacionales muestran que, en general, el afecto positivo es un factor protector de enfermedad cardiovascular, depresión, hostilidad y ansiedad.</p>
 
              <p>Además, se observó que las personas optimistas en comparación con las pesimistas padecían un 9% menos de enfermedad coronaria, 30% menos de mortalidad por coronariopatía y 14% menos de mortalidad total. Hallazgos similares observaron que el optimismo y la vitalidad emocional, se asociaron con una incidencia menor de enfermedad coronaria.</p>
 
              <p>El bienestar afectivo se asoció -tanto en mujeres como en hombres- con niveles menores de triglicéridos plasmáticos y mejor capacidad respiratoria. En varones se vinculó con un perímetro de cintura menor y niveles mayores de testosterona mientras que en mujeres se observó una concentración menor de proteína C reactiva y fibrinógeno y niveles mayores de colesterol HD, varios de estos factores se asocian con una reducción del riesgo de enfermedad vascular.</p>
              
