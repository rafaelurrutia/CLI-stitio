            Al tener que trabajar durante esta época del año, sin posibilidades de días libres para salir de la ciudad, las personas pueden perder el ánimo e incluso angustiarse. El deporte, el buen uso de las redes sociales y la mantención de las actividades que acostumbramos hacer, son algunas de las recomendaciones para tener una buena calidad laboral. <br><br>
 
            Mientras que muchas personas se toman vacaciones durante estos meses, otros continúan trabajando, lo que les puede llegar a generar ansiedad y estrés. Sin embargo, hay muchas formas de combatir estas sensaciones y hacer que estos meses en la ciudad sean agradables y entretenidos. <br><br>
             
            Una de las recomendaciones es realizar actividad física o al aire libre. “Siempre es bueno hacer deporte. Realizar actividades paralelas a lo laboral, entretenidas y que generen tanto bienestar como satisfacción, ayuda mucho a las personas que trabajan”, asegura Juan Pablo Westphal, psicólogo de Clínica Santa María. <br><br>
             
            Para que los días laborales durante los meses de enero y febrero se hagan más agradables, se deben realizar actividades del agrado de cada persona. Asimismo, se debe dar espacio para el descanso. “Los excesos producen más estrés y tensión, por lo que obligarse a salir y llenarse de actividades, al final sólo logra ansiedad y frustración”, aclara el psicólogo. <br><br>
             
            Por otro lado, es importante tener en cuenta que el manejo del estrés no sólo pasa por las distracciones ni los momentos de recreación, sino también por la forma en la que se distribuye la carga laboral y el respeto por los límites entre la concentración y la distracción. Esto significa que durante la jornada, se deben tener claro los tiempos de actividad, así como los momentos de pausa y el horario en que se termina el día de trabajo, para luego dar espacio a los momentos de ocio. <br><br>

            <strong>Importancia de mantener hábitos durante el año</strong> <br><br>

            El psicólogo Juan Pablo Westphal recomienda mezclar lo laboral con las distracciones y el descanso. “Es importante que el esfuerzo y ocio sean actividades constantes a lo largo de todo el año, llevando una vida de trabajo y entretención conjunta en el día a día”, explica. Por esto, no es aconsejable empezar o hacer cierto tipo de actividades sólo cuando se está de vacaciones, sino que intentar realizar estas actividades, ojalá, todas las semanas del año. De esta manera, es más difícil sentir una sensación de angustia y estrés al no poder salir de vacaciones durante el verano.
